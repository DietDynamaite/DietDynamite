-- 1. PLACES 테이블
CREATE TABLE PLACES (
    ID NUMBER CONSTRAINT PK_PLACES_PLACES_NO PRIMARY KEY,        
    NAME VARCHAR2(50) NOT NULL,  
    LATITUDE NUMBER(10, 7) NOT NULL,  
    LONGITUDE NUMBER(10, 7) NOT NULL,
    ADDRESS VARCHAR2(255) NOT NULL,
    PHONE VARCHAR2(20) NOT NULL
);
COMMENT ON COLUMN "PLACES"."ID" IS '장소 ID';
COMMENT ON COLUMN "PLACES"."NAME" IS '장소 이름';
COMMENT ON COLUMN "PLACES"."LATITUDE" IS '위도';
COMMENT ON COLUMN "PLACES"."LONGITUDE" IS '경도';
COMMENT ON COLUMN "PLACES"."ADDRESS" IS '장소 주소';
COMMENT ON COLUMN "PLACES"."PHONE" IS '장소 번호';

CREATE SEQUENCE SEQ_PLACES_NO NOCACHE;
