-- 본인이 포함되어있는 ChatRooms 모두 조회
SELECT CR.ROOM_NO, CR.ROOM_NAME,U.USER_ID,U.USER_IMG, TO_CHAR(CR.CREATE_DT, 'RRRR-MM-DD HH24:MM:SS') CREATE_DT, CR.CREATE_USER_NO, U.USER_NICKNAME, U.USER_EMAIL FROM CHAT_ROOM_MEMBER CRM
JOIN CHAT_ROOM CR ON (CR.ROOM_NO = CRM.ROOM_NO)
JOIN USER_INFO U ON (CR.CREATE_USER_NO = U.USER_NO)
WHERE CRM.USER_NO = 4;

-- CHATROOM 안에 있는 유저 조회
SELECT USER_EMAIL, USER_NICKNAME, USER_IMG FROM CHAT_ROOM_MEMBER
JOIN USER_INFO USING(USER_NO)
WHERE ROOM_NO = 3;

